// =====================
// Helper functions
// =====================

export function getTitleBySlug(slug: string): Title | undefined {
  return sampleTitles.find((t) => t.slug === slug);
}

export function getTitlesByType(type: string): Title[] {
  return sampleTitles.filter((t) => t.type === type);
}

export function getTitlesByGenre(genre: string): Title[] {
  return sampleTitles.filter((t) =>
    t.genres.some((g) => g.toLowerCase() === genre.toLowerCase())
  );
}

export function getTitlesByLanguage(language: string): Title[] {
  return sampleTitles.filter((t) =>
    t.languages.some((l) => l.toLowerCase() === language.toLowerCase())
  );
}

export function getTitlesByPlatform(platform: string): Title[] {
  return sampleTitles.filter((t) =>
    t.platforms.some((p) => p.toLowerCase() === platform.toLowerCase())
  );
}

export function getTrendingTitles(): Title[] {
  return sampleTitles.filter((t) => t.isTrending);
}

export function getLatestTitles(): Title[] {
  return sampleTitles.filter((t) => t.isLatest);
}

export function searchTitles(query: string): Title[] {
  const q = query.toLowerCase();
  return sampleTitles.filter(
    (t) =>
      t.title.toLowerCase().includes(q) ||
      t.overview.toLowerCase().includes(q) ||
      t.genres.some((g) => g.toLowerCase().includes(q)) ||
      t.director.toLowerCase().includes(q) ||
      t.cast.some((c) => c.name.toLowerCase().includes(q))
  );
}

export function getRelatedTitles(title: Title): Title[] {
  return title.relatedIds
    .map((id) => sampleTitles.find((t) => t.id === id))
    .filter((t): t is Title => t !== undefined);
}
